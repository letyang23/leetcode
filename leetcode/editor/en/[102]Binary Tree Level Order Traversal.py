# Helper Function
def to_binary_tree(items):
    if not items:
        return None

    it = iter(items)
    root = TreeNode(next(it))
    q = [root]
    for node in q:
        val = next(it, None)
        if val is not None:
            node.left = TreeNode(val)
            q.append(node.left)
        val = next(it, None)
        if val is not None:
            node.right = TreeNode(val)
            q.append(node.right)
    return root


node = [3, 9, 20, None, None, 15, 7]
root = to_binary_tree(node)
solution = Solution()
print(solution.levelOrder(root))
